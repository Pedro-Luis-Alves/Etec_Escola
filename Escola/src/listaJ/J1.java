package listaJ;

	/* Desenvolva  um  programa  para  apresentar  os  números  de  1  até  100  em  ordem  crescente  e 
	 * depois em ordem decrescente.
	 * */

public class J1 {

	public static void main(String[] args) {
	
		int num = 1;
		
		while (num <= 100){
			
				System.out.println(num);
				num++;
				}
		
		
			num = 100;
		
		while (num >= 1) {
			System.out.println(num);
			num--; 
		}
	
	}
		
	}

